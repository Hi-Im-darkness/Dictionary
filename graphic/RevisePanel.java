    private void startButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startButtonActionPerformed
        try {
            nQuestions = new Integer(nQuesInput.getText());
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Number of questions is not valid", "Error message", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (nQuestions <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Number of questions is not valid", "Error message", javax.swing.JOptionPane.ERROR_MESSAGE);            
            return;
        }
        
        if (! updateChoosen())
            return;

        parentPanel0.removeAll();
        parentPanel0.add(processPanel);
        parentPanel0.repaint();
        parentPanel0.revalidate();

        curQues = 1;
        jProgressBar1.setMaximum(nQuestions);
        Point.getInstance().resetPoint();
        runTest();
    }//GEN-LAST:event_startButtonActionPerformed

    private boolean updateChoosen() {
        boolean error = true;
        for (int i = 0; i < 6; i++) {
            if (listCheckBox.get(i).isSelected()) {
                choosen[i] = 1;
                error = false;
            } else
                choosen[i] = 0;
        }
        if (error) {
            javax.swing.JOptionPane.showMessageDialog(this, "You must choose at least one format of question.", "Error message", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
        return ! error;
    }
    
    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed
        if (curQues == nQuestions + 1) {
            int point = Point.getInstance().getPoint();
            String msg = String.format("Your result: %d / %d%n", point, nQuestions);
            if ((double)point / nQuestions <= 0.4) 
                msg += "Your should study English harder!!!";
            else if ((double)point / nQuestions >= 0.8)
                msg += "Impressionary, your English very good.";
            else
                msg += "Good test.";
            javax.swing.JOptionPane.showMessageDialog(this, msg, "Result", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            
            parentPanel0.removeAll();
            parentPanel0.add(startPanel);
            parentPanel0.repaint();
            parentPanel0.revalidate();
            
            return;
        }
        runTest();
    }//GEN-LAST:event_nextButtonActionPerformed

