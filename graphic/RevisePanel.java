    private void runTest() {
        progressLabel.setText(String.format("%d / %d", curQues, nQuestions));
        jProgressBar1.setValue(curQues);
        curQuesLabel.setText(String.format("%d", curQues));
        
        int index = 0;
        do {
            index = SwingUtil.randomMachine.nextInt(listFormatTest.size());
        } while (choosen[index] == 0);
        AbstractPanel choosenFormat = listFormatTest.get(index);
        parentPanel1.removeAll();
        parentPanel1.add(choosenFormat);
        parentPanel1.repaint();
        parentPanel1.revalidate();
        choosenFormat.loadQuestion();
        
        curQues++;
    }

