    private void delButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delButtonActionPerformed
        if (historyList.getSelectedValue() != null) {
            int tmp = historyList.getSelectedIndex();
            Scanner split = new Scanner(historyList.getSelectedValue());
            SwingUtil.dict.his.deleteWord(split.next());
            updateHistory();
            if (tmp == historyList.getModel().getSize())
                tmp--;
            historyList.setSelectedIndex(tmp);
        }
    }//GEN-LAST:event_delButtonActionPerformed

