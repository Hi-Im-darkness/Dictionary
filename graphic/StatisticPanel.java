    public void updateHistory() {
        SwingUtil.dict.sts.setWidth(-24);
        if (filterBox.getSelectedIndex() == 0)
            displayAllWords();
        else if (filterBox.getSelectedIndex() == 2)
            displayOldestWords();
        else
            displayTodayWords();
    }
    
    private void displayAllWords() {
        historyList.setListData(SwingUtil.dict.sts.allLearnedWord());
        totalLabel.setText(String.format("Total : %3d word(s)", SwingUtil.dict.sts.allLearnedWord().length));
    }
    
    private void displayOldestWords() {
        historyList.setListData(SwingUtil.dict.sts.oldestWord(10));
        totalLabel.setText(String.format("Total : %3d word(s)", SwingUtil.dict.sts.oldestWord(10).length));
    }
    
    private void displayTodayWords() {
        historyList.setListData(SwingUtil.dict.sts.wordSearchToday());
        totalLabel.setText(String.format("Total : %3d word(s)", SwingUtil.dict.sts.wordSearchToday().length));
    }

