    private void okButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButton1ActionPerformed
        String word = wordInput.getText();
        String pronun = pronunInput.getText();
        String mean = meanInput.getText();
        if (word.length() * pronun.length() * mean.length() != 0) {
            SwingUtil.dict.vMng.vocAt(curVocIndex).addWord(word, mean, pronun);
            updateVocInfo();
            addWordDialog.dispose();
            javax.swing.JOptionPane.showMessageDialog(this, "Add word sucessfully");
        } else 
            javax.swing.JOptionPane.showMessageDialog(this, "Name, Pronunciation and Mean is not empty", "Error message", javax.swing.JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_okButton1ActionPerformed

    private void okButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButton2ActionPerformed
        String key = (String)delWord.getSelectedItem();
        if (key.length() == 0 || SwingUtil.dict.vMng.vocAt(curVocIndex).deleteWord(key) == 404)
            javax.swing.JOptionPane.showMessageDialog(this, "Word not found", "Error message", javax.swing.JOptionPane.ERROR_MESSAGE);
        else {
            updateVocInfo();
            delWDialog.dispose();
            javax.swing.JOptionPane.showMessageDialog(this, "Delete word sucessfully");
        }
    }//GEN-LAST:event_okButton2ActionPerformed

    private void mergeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mergeButtonActionPerformed
        int[] listIndices = listVoc.getSelectedIndices();
        if (listIndices.length == 2) {
            int srcIndex = listVoc.getLeadSelectionIndex();
            int dstIndex = listIndices[0] + listIndices[1] - srcIndex;            
            Vocabulary src = SwingUtil.dict.vMng.vocAt(srcIndex);
            Vocabulary dst = SwingUtil.dict.vMng.vocAt(dstIndex);
            dst.merge(src);
            curVocIndex = srcIndex;
            delButtonActionPerformed(null);
            listVoc.setSelectedIndex(dstIndex);
            updateVocInfo();
            String msg = String.format("Merge %s to %s successfully", src.getName().toUpperCase(), dst.getName().toUpperCase());
            javax.swing.JOptionPane.showMessageDialog(this, msg);
        }
    }//GEN-LAST:event_mergeButtonActionPerformed

