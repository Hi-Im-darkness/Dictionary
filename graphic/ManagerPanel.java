    private void delWButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delWButtonActionPerformed
        for (String s: SwingUtil.dict.vMng.vocAt(curVocIndex).getKeyword())
            delWord.addItem(s);
        delWord.setSelectedIndex(-1);
        AutoCompleteDecorator.decorate(delWord);
        delWDialog.setVisible(true);
    }//GEN-LAST:event_delWButtonActionPerformed

    private void delButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delButtonActionPerformed
        SwingUtil.dict.vMng.removeVocabulary(curVocIndex);
        int newChosen = curVocIndex;
        if (curVocIndex == SwingUtil.dict.vMng.size())
            newChosen--;
        updateListVoc();
        listVoc.setSelectedIndex(newChosen);
        updateVocInfo();
    }//GEN-LAST:event_delButtonActionPerformed

    private void importButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_importButtonActionPerformed
        javax.swing.JFileChooser importWindow = new javax.swing.JFileChooser();
        importWindow.setCurrentDirectory(new File("/home/darkness"));
        importWindow.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Microsoft Excel 97 - 2003 (*.xls)", "xls"));
        importWindow.setDialogTitle("Import data");
        importWindow.showOpenDialog(null);
        
        File selectFile = importWindow.getSelectedFile();
        if (selectFile != null) {
            SwingUtil.dict.vMng.vocAt(curVocIndex).loadFromFile(selectFile.getPath());
            int tmp = curVocIndex;
            updateListVoc();
            listVoc.setSelectedIndex(tmp);
            updateVocInfo();
            javax.swing.JOptionPane.showMessageDialog(this, "Import data successfully");
        }
    }//GEN-LAST:event_importButtonActionPerformed

