    private void updateVocInfo() {
        if (curVocIndex == -1) {
            nameLabel.setText("");
            nWordsLabel.setText("");
            importLabel.setText("");
        }
        else {
            ArrayList<String> vocInfo = SwingUtil.dict.vMng.vocAt(curVocIndex).getInfo();
            nameLabel.setText(vocInfo.get(0));
            nWordsLabel.setText(vocInfo.get(1));
            importLabel.setText(vocInfo.get(2));
        }
    }

